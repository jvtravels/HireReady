-- 1. Profiles (extends auth.users)
create table if not exists profiles (
  id uuid references auth.users on delete cascade primary key,
  name text not null default '',
  email text not null default '',
  target_role text default '',
  target_company text default '',
  industry text default '',
  interview_date text default '',
  experience_level text default '',
  learning_style text default 'direct',
  resume_file_name text default '',
  resume_text text default '',
  practice_timestamps jsonb default '[]'::jsonb,
  avatar_url text default '',
  subscription_tier text default 'free' check (subscription_tier in ('free', 'starter', 'pro')),
  subscription_start timestamptz,
  subscription_end timestamptz,
  cancel_at_period_end boolean default false,
  subscription_paused boolean default false,
  razorpay_payment_id text,
  razorpay_subscription_id text,
  has_completed_onboarding boolean default false,
  referral_code text unique,
  referred_by text,
  preferred_session_length integer,
  interview_types jsonb default '[]'::jsonb,
  resume_data jsonb,
  created_at timestamptz default now()
);

-- 2. Interview sessions
create table if not exists sessions (
  id text primary key,
  user_id uuid references profiles(id) on delete cascade not null,
  date timestamptz default now(),
  type text not null,
  difficulty text not null,
  focus text default '',
  duration integer default 0,
  score integer default 0,
  questions integer default 0,
  transcript jsonb default '[]'::jsonb,
  ai_feedback text default '',
  skill_scores jsonb,
  -- Results report (generated by /api/evaluate-session, cached for re-open)
  report_json jsonb,
  report_version text default 'mvp-1',
  report_generated_at timestamptz,
  created_at timestamptz default now()
);
-- Ensure additive columns exist on upgrades (safe, idempotent)
alter table sessions add column if not exists report_json jsonb;
alter table sessions add column if not exists report_version text default 'mvp-1';
alter table sessions add column if not exists report_generated_at timestamptz;
create index if not exists idx_sessions_report_generated on sessions(report_generated_at);

-- Resume v2 binding — every session captures the exact resume_version
-- used at session start so question generation, scoring, and feedback
-- reference an immutable snapshot. NULL on legacy rows; new rows always
-- populated. Set to NULL on cascade rather than blocking so deleting an
-- old resume doesn't trash session history.
alter table sessions add column if not exists resume_version_id uuid;
create index if not exists idx_sessions_resume_version on sessions(resume_version_id);

-- 3. Calendar events
create table if not exists calendar_events (
  id text primary key,
  user_id uuid references profiles(id) on delete cascade not null,
  title text not null,
  company text default '',
  date text not null,
  time text default '',
  type text default 'interview',
  notes text default '',
  created_at timestamptz default now()
);

-- 4. Feedback
create table if not exists feedback (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references profiles(id) on delete cascade not null,
  session_id text references sessions(id) on delete cascade not null,
  rating text check (rating in ('helpful', 'too_harsh', 'too_generous', 'inaccurate')) not null,
  comment text default '',
  session_score integer default 0,
  session_type text default '',
  created_at timestamptz default now()
);

-- 5. Payments
create table if not exists payments (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references profiles(id) on delete cascade not null,
  razorpay_payment_id text unique not null,
  razorpay_order_id text default '',
  plan text not null,
  tier text not null,
  amount integer not null,
  currency text default 'INR',
  status text default 'completed',
  subscription_start timestamptz,
  subscription_end timestamptz,
  created_at timestamptz default now()
);

